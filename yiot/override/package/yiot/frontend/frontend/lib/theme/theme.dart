import 'package:flutter/material.dart';
import 'package:yiot_portal/utils/responsive-layout.dart';

class YIoTTheme {
  static const int _blackPrimaryValue = 0xFF000000;

  static const MaterialColor mainColor = MaterialColor(
    _blackPrimaryValue,
    <int, Color>{
      50: Color(0xFF000000),
      100: Color(0xFF000000),
      200: Color(0xFF000000),
      300: Color(0xFF000000),
      400: Color(0xFF000000),
      500: Color(_blackPrimaryValue),
      600: Color(0xFF000000),
      700: Color(0xFF000000),
      800: Color(0xFF000000),
      900: Color(0xFF000000),
    },
  );

  static const MaterialColor secondaryColor = Colors.grey;

  static const Color backgroundColor = Color(0xFFEEEEEE);
  static const Color iconColor = Colors.black87;
  static const Color iconColorInverted = Colors.white;
  static const Color activeIconColor = Colors.blue;
  static const Color menuFontColor = Color(0xFF337ab7);
  static const Color borderColor = Color(0xFFE7E7E7);

  static double fontH1(context) {
    return ResponsiveLayout.fixGeometrySz(context, 35);
  }

  static double fontH2(context) {
    return ResponsiveLayout.fixGeometrySz(context, 20);
  }

  static double fontH3(context) {
    return ResponsiveLayout.fixGeometrySz(context, 16);
  }

  static double fontH4(context) {
    return ResponsiveLayout.fixGeometrySz(context, 13);
  }

  static ThemeData current() {
    return ThemeData(primarySwatch: YIoTTheme.mainColor);
  }
}
