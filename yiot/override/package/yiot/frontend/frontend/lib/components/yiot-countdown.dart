import 'package:quiver/async.dart';

typedef OnPeriod = void Function(YIoTCountdown timer, int currentMs);
typedef OnDeadline = void Function();

// -----------------------------------------------------------------------------
class YIoTCountdown extends CountdownTimer {
  late final int periodMs;
  late final int deadlineMs;
  OnPeriod? onPeriod;
  OnDeadline? onDeadline;

  YIoTCountdown(
      {required this.periodMs,
      required this.deadlineMs,
      this.onPeriod,
      this.onDeadline})
      : super(
          new Duration(seconds: deadlineMs),
          new Duration(milliseconds: periodMs),
        ) {
    var sub = super.listen(null);
    sub.onData((duration) {
      if (this.onPeriod != null) {
        this.onPeriod!(this, duration.elapsed.inMilliseconds);
      }
    });

    sub.onDone(() {
      if (this.onDeadline != null && super.isRunning) {
        this.onDeadline!();
      }
      sub.cancel();
    });
  }
}

