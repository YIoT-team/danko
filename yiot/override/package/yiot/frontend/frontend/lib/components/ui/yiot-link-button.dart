import 'package:flutter/material.dart';
import 'package:flutter/gestures.dart';

import 'package:yiot_portal/theme/theme.dart';
import 'package:yiot_portal/utils/responsive-layout.dart';

// -----------------------------------------------------------------------------

class YIoTLinkButton extends StatelessWidget {
  final String text;
  VoidCallback? onPressed;
  late final Color color;

  YIoTLinkButton(
      {required this.text, this.onPressed, this.color = Colors.blue});

  @override
  Widget build(BuildContext context) {
    return Container(
      padding: const EdgeInsets.all(10.0),
      child: RichText(
        text: TextSpan(
          text: text,
          style:
              new TextStyle(fontSize: YIoTTheme.fontH3(context), color: color),
          recognizer: TapGestureRecognizer()..onTap = onPressed,
        ),
      ),
    );
  }
}

