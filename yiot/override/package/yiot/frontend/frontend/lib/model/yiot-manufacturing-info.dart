import 'package:yiot_portal/model/yiot-license.dart';

class YIoTManufacturingInfo {
  late final YIoTLicense license;
  late final String icon;
  late final String docLink;
  late final String explain;

  YIoTManufacturingInfo({
    required this.license,
    required this.icon,
    required this.docLink,
    required this.explain,
  });

  static YIoTManufacturingInfo blank() {
    return YIoTManufacturingInfo(
      license: YIoTLicense.blank(),
      icon: "",
      docLink: "",
      explain: "",
    );
  }
}

