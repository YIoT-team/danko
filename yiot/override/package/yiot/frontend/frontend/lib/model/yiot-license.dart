import 'package:yiot_portal/model/yiot-device.dart';

class YIoTLicense {
  late final String full;
  late final YIoTDevice deviceInfo;

  YIoTLicense({
    required this.full,
    required this.deviceInfo,
  });

  static YIoTLicense blank() {
    return YIoTLicense(
      full: "",
      deviceInfo: YIoTDevice.blank(),
    );
  }

  bool valid() {
    return !full.isEmpty && deviceInfo.valid();
  }
}

