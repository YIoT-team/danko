class YIoTDevice {
  late final String manufacturer;
  late final String model;
  late final String serial;
  late final String macAddress;
  late final String publicKey;
  late final String initialUser;
  late final String initialPassword;
  late final String initialAddress;

  YIoTDevice(
      {required this.manufacturer,
      required this.model,
      required this.serial,
      required this.macAddress,
      required this.publicKey,
      required this.initialUser,
      required this.initialPassword,
      required this.initialAddress});

  static YIoTDevice blank() {
    return YIoTDevice(
        manufacturer: "",
        model: "",
        serial: "",
        macAddress: "",
        publicKey: "",
        initialUser: "",
        initialPassword: "",
        initialAddress: "");
  }

  bool valid() {
    return !manufacturer.isEmpty &&
        !model.isEmpty &&
        !serial.isEmpty &&
        !macAddress.isEmpty &&
        !publicKey.isEmpty &&
        !initialUser.isEmpty &&
        !initialPassword.isEmpty &&
        !initialAddress.isEmpty;
  }
}

