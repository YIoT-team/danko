SCRIPT_PATH="$(cd $(dirname "$0") >/dev/null 2>&1 && pwd)"
source "${SCRIPT_PATH}/../docker/SETTINGS"
source ${SCRIPT_PATH}/inc/helpers.sh

BASE_PATH="$(realpath ${SCRIPT_PATH}/../..)"
PARAM_CPU="x86_64"
CV2SE_ARTIFACTS_PATH="${BASE_PATH}/build-${PARAM_CPU}/build-artifacts"

# -----------------------------------------------------------------------------
gen_vdi() {
    pushd "${CV2SE_ARTIFACTS_PATH}"
      rm -f ${1} || true
      _new_dir "tmp"
      pushd "tmp"
        cp ../${1}.gz ./
        gzip -d ${1}.gz
        qemu-img convert -f raw -O vdi ${1} ../${1}.vdi
      popd

      rm -rf "tmp"
    popd
}

# -----------------------------------------------------------------------------

gen_vdi "openwrt-x86-64-generic-squashfs-combined-efi.img"

